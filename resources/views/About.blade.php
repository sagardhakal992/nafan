        <h3>2. Rationale – why NAFAN was needed?</h3>
        <p style="font-size: 120%">After political changes in 1990, some non-governmental organizations advocated and persuaded the government to change some policies and legislations relating to forestry, women's property rights, and consumer protection issues. Some of these policy changes brought good impacts on the protection of rights of the underprivileged, marginalized communities and women. Some of the groups launched movements for ensuring access to natural resources and benefit sharing. Some organizations raised issues of gender and equity, and some others raised the issues of bounded labor and so on. All of such organizations and groups faced a tough time during the movements. Therefore, such members of civil society organizations realized that Public Advocacy in isolation did not produce the intended results. Since then the need for a national-level forum as a resource organization for public advocacy on various issues was strongly felt. </p>
        <br />
        <p style="font-size: 120%">However, the northern part of India, Pakistan, Bangladesh, and the whole of Nepal did not have adequate knowledge and skills in public advocacy by that time. Therefore, the initiative of ICIMOD was to bring advocacy expertise from the South to the North in these countries. Such knowledge and skills transformation were also not possible without a professional forum. These are some of the rationales for establishing NAFAN. </p>
